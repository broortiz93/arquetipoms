package com.javanes.framework.model;

public class Banco {
	private String ccodret;
	private String cvebanco;
	private String descripcion;
	private String tipobanco;
	private Integer cvesif;
	private String nombrecorto;
	private String flagdomir;
	private String flagdomip;
	
	public String getCcodret() {
		return ccodret;
	}
	public void setCcodret(String ccodret) {
		this.ccodret = ccodret;
	}
	public String getCvebanco() {
		return cvebanco;
	}
	public void setCvebanco(String cvebanco) {
		this.cvebanco = cvebanco;
	}
	public String getTipobanco() {
		return tipobanco;
	}
	public void setTipobanco(String tipobanco) {
		this.tipobanco = tipobanco;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public Integer getCvesif() {
		return cvesif;
	}
	public void setCvesif(Integer cvesif) {
		this.cvesif = cvesif;
	}
	public String getNombrecorto() {
		return nombrecorto;
	}
	public void setNombrecorto(String nombrecorto) {
		this.nombrecorto = nombrecorto;
	}
	public String getFlagdomir() {
		return flagdomir;
	}
	public void setFlagdomir(String flagdomir) {
		this.flagdomir = flagdomir;
	}
	public String getFlagdomip() {
		return flagdomip;
	}
	public void setFlagdomip(String flagdomip) {
		this.flagdomip = flagdomip;
	}
}